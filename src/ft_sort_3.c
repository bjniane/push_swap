#include "push_swap.h"

void	ft_sort_3(t_stack **a)
{
	if ((*a)->data == ft_max(*a))
		ft_ra(a, 0);
	else if ((*a)->next->data == ft_max(*a))
		ft_rra(a, 0);
	if ((*a)->data > (*a)->next->data)
		ft_sa(a, 0);
}
